package com.atguigu.springcloud.alibaba.domain;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * @author johnny
 * @Classname CommonResult
 * @Description
 * @Date 2022/3/10 9:41 上午
 */
